WHERE TABLE_NAME = 'T_TESTE_DDL';	 -- WHERE (CONDIÇÃO) - INDICA O FILTRO A SER REALIZADO
									-- EVITA RECUPERAR TODAS AS LINHAS DA TABELA
									
-- COMO APAGAR UMA COLUNA DA TABELA?
-- OPÇÃO: DROP
-- ELIMINAR A COLUNA DS_OBS2
ALTER TABLE T_TESTE_DDL
	DROP COLUMN DS_OBS2;
	
-- COMO APAGAR VÁRIAS COLUNAS DA TABELA
-- ELIMINAR A COLUNA DS_TESTE e DS_OBS1
ALTER TABLE T_TESTE_DDL
	DROP (DS_TESTE, DS_OBS1);		
	
-- COMO APAGAR UMA CONSTRAINT CHAVE PRIMARIA
-- UTILIZAMOS A OPÇÃO: DROP CONSTRAINT
-- PONTO DE ATENÇÃO: PARA OS RELACIONAMENTOS, ONDE UMA
-- CHAVE ESTRANGEIRA APONTA PARA UMA CHAVE PRIMARIA, PRECISAMOS
-- NO MOMENTO QUE APAGAR A CONSTRAINT, ELIMINAR O RELACIONAMENTO
-- PARA ELIMINAR O VINCULO DO RELACIONAMENTO,
-- PRECISAMOS INCLUIR A OPÇÃO CASCADE (ELIMINAR O RELACIONAMENTO)
ALTER TABLE T_TESTE_DDL
	DROP CONSTRAINT PK_TESTE_DDL; -- QUANDO NÃO EXISTIR RELACIONAMENTO

ALTER TABLE T_TESTE_DDL
	DROP CONSTRAINT PK_TESTE_DDL CASCADE; -- QUANDO EXISTIR RELACIONAMENTO (APENAS NA PK QUE PODE EXISTIR RELACIONAMENTO E ASSIM UTILIZAR O CASCADE)

